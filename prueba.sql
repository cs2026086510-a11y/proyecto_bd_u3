WHERE DEPARTMENT_ID=1;

DELETE FROM ENROLLMENTS
WHERE GRADE < 13;

SELECT
    STUDENT_ID,
    FIRST_NAME,
    LAST_NAME,
    CASE
        WHEN STATUS='ACTIVE'
            THEN 'ENROLLED'
        ELSE 'NOT ENROLLED'
    END AS CURRENT_STATUS
FROM STUDENTS;

SELECT
    P.FIRST_NAME,
    P.LAST_NAME,
    COUNT(C.COURSE_ID) AS COURSES_ASSIGNED
FROM PROFESSORS P
LEFT JOIN COURSES C
ON P.PROFESSOR_ID=C.PROFESSOR_ID
GROUP BY
    P.PROFESSOR_ID,
    P.FIRST_NAME,
    P.LAST_NAME;

SELECT
    D.NAME,
    AVG(P.SALARY)
FROM DEPARTMENTS D
INNER JOIN PROFESSORS P
ON D.DEPARTMENT_ID=P.DEPARTMENT_ID
GROUP BY D.NAME;

SELECT
    S.FIRST_NAME,
    S.LAST_NAME,
    SUM(C.CREDITS) AS TOTAL_CREDITS
FROM STUDENTS S
INNER JOIN ENROLLMENTS E
ON S.STUDENT_ID=E.STUDENT_ID
INNER JOIN COURSES C
ON C.COURSE_ID=E.COURSE_ID
GROUP BY
    S.STUDENT_ID,
    S.FIRST_NAME,
    S.LAST_NAME;

SELECT
    FIRST_NAME,
    LAST_NAME
FROM STUDENTS
WHERE STUDENT_ID IN
(
    SELECT STUDENT_ID
    FROM ENROLLMENTS
    WHERE GRADE >= 18
);

SELECT
    NAME
FROM COURSES
WHERE COURSE_ID NOT IN
(
    SELECT COURSE_ID
    FROM ENROLLMENTS
);

