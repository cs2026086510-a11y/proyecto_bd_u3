SELECT
    FIRST_NAME,
    LAST_NAME,
    EMAIL
FROM STUDENTS
ORDER BY LAST_NAME;

SELECT
    NAME,
    CREDITS
FROM COURSES
WHERE CREDITS >= 4;

SELECT
    S.FIRST_NAME,
    S.LAST_NAME,
    C.NAME
FROM STUDENTS S
INNER JOIN ENROLLMENTS E
    ON S.STUDENT_ID = E.STUDENT_ID
INNER JOIN COURSES C
    ON C.COURSE_ID = E.COURSE_ID;

SELECT
    P.FIRST_NAME,
    P.LAST_NAME,
    D.NAME
FROM PROFESSORS P
INNER JOIN DEPARTMENTS D
ON P.DEPARTMENT_ID = D.DEPARTMENT_ID;

SELECT
    C.NAME,
    COUNT(E.ENROLLMENT_ID) AS TOTAL_STUDENTS
FROM COURSES C
LEFT JOIN ENROLLMENTS E
ON C.COURSE_ID = E.COURSE_ID
GROUP BY C.NAME
ORDER BY TOTAL_STUDENTS DESC;

SELECT
    AVG(GRADE) AS AVERAGE_GRADE
FROM ENROLLMENTS;

SELECT
    MAX(GRADE),
    MIN(GRADE)
FROM ENROLLMENTS;

SELECT
    STATUS,
    COUNT(*)
FROM STUDENTS
GROUP BY STATUS;

UPDATE STUDENTS
