CREATE TABLE empleados (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nombre VARCHAR2(100) NOT NULL,
    apellido VARCHAR2(100) NOT NULL,
    edad NUMBER(3),
    salario NUMBER(10,2),
    fecha_ingreso DATE,
    activo CHAR(1) DEFAULT 'S',
    CONSTRAINT pk_empleados PRIMARY KEY (id)
);

