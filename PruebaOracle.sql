WHERE edad > 65;

SELECT
    e.id,
    e.nombre,
    d.nombre_departamento
FROM empleados e
INNER JOIN departamentos d
    ON e.departamento_id = d.id;

SELECT
    departamento_id,
    COUNT(*) AS total_empleados,
    AVG(salario) AS salario_promedio
FROM empleados
GROUP BY departamento_id;

SELECT
    nombre,
    salario
FROM empleados
